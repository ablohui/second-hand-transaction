package com.yh.valuebeen;

public class Car {
	private String carid;
	private String useridsell;
	private String useridadrop;
	private String shopid;
	private Double price;
	private String datetime;
	private String status;
	private String address;
	private String comment;
	public String getCarid() {
		return carid;
	}
	public void setCarid(String carid) {
		this.carid = carid;
	}
	public String getUseridsell() {
		return useridsell;
	}
	public void setUseridsell(String useridsell) {
		this.useridsell = useridsell;
	}
	public String getUseridadrop() {
		return useridadrop;
	}
	public void setUseridadrop(String useridadrop) {
		this.useridadrop = useridadrop;
	}
	public String getShopid() {
		return shopid;
	}
	public void setShopid(String shopid) {
		this.shopid = shopid;
	}
	public Double getPrice() {
		return price;
	}
	public void setPrice(Double price) {
		this.price = price;
	}
	public String getDatetime() {
		return datetime;
	}
	public void setDatetime(String datetime) {
		this.datetime = datetime;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getComment() {
		return comment;
	}
	public void setComment(String comment) {
		this.comment = comment;
	}

}
